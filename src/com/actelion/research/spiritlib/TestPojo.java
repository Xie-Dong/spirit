package com.actelion.research.spiritlib;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class TestPojo implements Serializable {
	
	private int id;
	private String category;
	private String name;
	private List<TestAttributePojo> attributes = new ArrayList<>();
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<TestAttributePojo> getAttributes() {
		return attributes;
	}
	public void setAttributes(List<TestAttributePojo> attributes) {
		this.attributes = attributes;
	}

	@Override
	public String toString() {
		return "[TestPojo: "+name+"]";
	}
	
}
